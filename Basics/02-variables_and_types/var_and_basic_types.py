# # : 주석을 나타내는 기호로, #으로 시작하는 줄은 전부 주석처리되며 코드로 인식되지 않습니다.

# 변수란, 데이터를 담는 상자입니다. 변수마다 고유한 이름을 지정해 쓰고, 그 변수의 공간에 데이터를 저장합니다.
# 파이썬에서 변수를 선언할 때는 [ 변수명 = 데이터 ] 의 형태를 사용합니다.
var = 5
var = "Hello World!"

# int : 정수형 타입(자료형)으로, 정수 데이터를 저장합니다.
number = 0
# float : 실수형 타입(자료형)으로, 실수 데이터 (소수점 있는 수)를 저장합니다.
real_number = 3.14
# str : 문자열 타입(자료형)으로, 문자 및 문자열 데이터를 저장합니다. "" 또는 ''로 표현합니다.
text = "안녕하세요"
char = 'O'
# bool : 진리형 타입(자료형)으로, 참 혹은 거짓의 값을 저장합니다. 참은 True, 거짓은 False로 표현됩니다.
boolean = True

